#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	if (s1 != NULL && s2 != NULL)
	{
		while ((s1[i] || s2[i]) && n)
		{
			if (s1[i] != s2[i])
				return (0);
			n--;
			i++;
		}
	}
	return (1);
}
